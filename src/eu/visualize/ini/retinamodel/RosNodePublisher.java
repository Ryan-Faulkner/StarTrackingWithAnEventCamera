/**
 * RosJava node supplying data to ROS master.
 * This publisher supplies the X,Y,Z locations of the target (prey) but has no option for "prey not visible".
 * 
 * 
 * 
 @author diederik
*/
@Description("RosJava node supplying data to ROS master")
public class RosNodePublisher {
    int xCoordinate;
    int yCoordinate;
    float zCoordinate;
//----------------------------------------------------------------------------//
//-- X coordinate set --------------------------------------------------------//
//----------------------------------------------------------------------------// 
    public void setXcoordinate(int xCoord) {//receive the x pixel coordinate of center of mass
        this.xCoordinate = xCoord;
        System.out.println(xCoordinate);
    }
//----------------------------------------------------------------------------//
//----------------------------------------------------------------------------//
    
//----------------------------------------------------------------------------//
//-- X coordinate get --------------------------------------------------------//
//----------------------------------------------------------------------------// 
    public int getXcoordinate() {//get the x pixel coordinate of center of mass
        return xCoordinate;
    }
//----------------------------------------------------------------------------//
//----------------------------------------------------------------------------//
    
//----------------------------------------------------------------------------//
//-- Y coordinate set --------------------------------------------------------//
//----------------------------------------------------------------------------// 
    public void setYcoordinate(int yCoord) {//receive the y pixel coordinate of center of mass
        this.yCoordinate = yCoord;
        System.out.println(yCoordinate);
    }
//----------------------------------------------------------------------------//
//----------------------------------------------------------------------------//
    
//----------------------------------------------------------------------------//
//-- Y coordinate set --------------------------------------------------------//
//----------------------------------------------------------------------------// 
    public int getYcoordinate() {//get the y pixel coordinate of center of mass
        return yCoordinate;
    }
//----------------------------------------------------------------------------//
//----------------------------------------------------------------------------//
    
//----------------------------------------------------------------------------//
//-- Z coordinate (distance) set ---------------------------------------------//
//----------------------------------------------------------------------------// 
    public void setZcoordinate(float zCoord) {//receive the estimated distance of object
        this.zCoordinate = zCoord;
        System.out.println(zCoordinate);
    }
//----------------------------------------------------------------------------//
//----------------------------------------------------------------------------//
    
//----------------------------------------------------------------------------//
//-- Z coordinate (distance) get ---------------------------------------------//
//----------------------------------------------------------------------------// 
    public float getZcoordinate() {//get the estimated distance of object
        return zCoordinate;
    }
//----------------------------------------------------------------------------//
//----------------------------------------------------------------------------//
}
